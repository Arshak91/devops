#!bin/bash



if [[ $1 =~ ^((1|[1-9]{2}|2[0-4][0-9]|25[0-5])\.)$ ]];
then
    echo $1
fi

validIp $1
function validIp() {
    ip=$1

    if [[ $ip =~ ^(([0-9]|1?[0-9]?[0-9]{2}|2[0-4][0-9]|25[0-5])\.(([0-9]|1?[0-9]?[0-9]{2}|2[0-4][0-9]|25[0-5])\.){2}([0-9]|1?[0-9]?[0-9]{2}|2[0-4][0-9]|25[0-4]))$ ]];
    then
        echo $ip
    else
        echo "IP isn't valid"
    fi
}

